//Chapter 05: Math expressions:- Familiar Operators

// Wherever you can use a number, you can use a math expression. For example, you're
// familiar with this kind of statement.

var add = 5 + 5;
console.log(add); // 10

var subtract = 4 - 3;
console.log(subtract); // 1

var multiply = 4 * 5;
console.log(multiply); //20

var divide = 3 / 5;
console.log(divide);  // 0.6

var result = divide + add ;
console.log(result); // 10.6

